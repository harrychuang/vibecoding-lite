/**
 * SocialIcon component displays social media platform icons.
 *
 * Features:
 * - Multiple social media platforms supported
 * - Two variants: colored (brand colors) and white
 * - Custom color support for single-color customization
 * - Accessible with proper aria labels
 */
export const SocialIcon: React.FC<SocialIconProps> = ({
  platform,
  variant = 'colored',
  color,
  size = 'default',
  className = '',
  ariaLabel,
}) => {
  const icon = icons[platform];
  const label = ariaLabel || `${platform} icon`;

  // Determine if we're using custom color
  const hasCustomColor = !!color;

  const containerClasses = [
    styles.socialIcon,
    !hasCustomColor && styles[variant],
    hasCustomColor && styles.customColor,
    styles[size],
    className,
  ]
    .filter(Boolean)
    .join(' ');

  // Determine the fill color
  const getFillColor = () => {
    if (hasCustomColor) return color;
    if (variant === 'white') return 'currentColor';
    return icon.colored;
  };

  // For custom color or white variant, we need to override all fills
  const svgStyle = (hasCustomColor || variant === 'white') 
    ? { fill: getFillColor() } 
    : undefined;

  return (
    <span className={containerClasses} role="img" aria-label={label}>
      <svg
        viewBox="0 0 24 24"
        fill={hasCustomColor ? color : (variant === 'colored' ? undefined : 'currentColor')}
        className={styles.svg}
        xmlns="http://www.w3.org/2000/svg"
        style={svgStyle}
      >
        {hasCustomColor ? (
          <g fill={color}>{icon.path}</g>
        ) : variant === 'colored' ? (
          <g fill={icon.colored}>{icon.path}</g>
        ) : (
          <g>{icon.path}</g>
        )}
      </svg>
    </span>
  );
};

export default SocialIcon;
